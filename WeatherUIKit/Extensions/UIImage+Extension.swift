import UIKit

extension UIImage {
    static let thunderstorm = UIImage(named: "imThunderstorm")
    static let rain = UIImage(named: "imRain")
    static let drizzle = UIImage(named: "imDrizzle")
    static let snow = UIImage(named: "imSnow")
    static let atmosphere = UIImage(named: "imAtmosphere")
    static let clear = UIImage(named: "imClear")
    static let clouds = UIImage(named: "imClouds")
}
